    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String nik = txtnik.getText();
        String namalengkap = txtnamalengkap.getText();
        String tempatlahir = txttempatlahir.getText();
        String tanggallahir = txttanggallahir.getText();
        String jeniskelamin = null;
        if (rblaki.isSelected()) {
            jeniskelamin = "Laki-Laki";
        } else if (rbperempuan.isSelected()) {
            jeniskelamin = "Perempuan";
        }
        String kewarganegaraan = txtkewarganegaraan.getText();
        String agama = (String) cbagama.getSelectedItem();
        String notelepon = txtnotelepon.getText();
        String email = txtemail.getText();
        String asalsekolah = txtasalsekolah.getText();
        String alamat = txtalamat.getText();
        String kodepos = txtkodepos.getText();
        String provinsi = txtprovinsi.getText();
        String kabkota = txtkabkota.getText();
        String kecamatan = txtkecamatan.getText();
        String pilihan1 = (String) cbpilihan1.getSelectedItem();
        String pilihan2 = (String) cbpilihan2.getSelectedItem();
        String pilihan3 = (String) cbpilihan3.getSelectedItem();
        if (txtnik.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon Masukkan Data untuk di Input", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
        } else {
             try {
                 java.sql.Statement stat = koneksi.createStatement();
                 ResultSet data = stat.executeQuery("SELECT * FROM mahasiswabaru WHERE NIK ='" + txtnik.getText() + "'");
                 if (data.next()) {
                     JOptionPane.showMessageDialog(null, "NIK Sudah Ada", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                     txtnik.requestFocus();
                 } else {
                    String sql = "INSERT INTO mahasiswabaru VALUES('" + nik + "'"
                           + ",'" + namalengkap + "'"
                           + ",'" + tempatlahir + "'"
                           + ",'" + tanggallahir + "'"
                           + ",'" + jeniskelamin + "'"
                           + ",'" + kewarganegaraan + "'"
                           + ",'" + agama + "'"
                           + ",'" + notelepon + "'"
                           + ",'" + email + "'"
                           + ",'" + asalsekolah + "'"
                           + ",'" + alamat + "'"
                           + ",'" + kodepos + "'"
                           + ",'" + provinsi + "'"
                           + ",'" + kabkota + "'"
                           + ",'" + kecamatan + "'"
                           + ",'" + pilihan1 + "'"
                           + ",'" + pilihan2 + "'"
                           + ",'" + pilihan3 + "')";
                    stat.executeUpdate(sql);
                    txtnik.setText("");
                    txtnamalengkap.setText("");
                    txttempatlahir.setText("");
                    txttanggallahir.setText("");
                    txtkewarganegaraan.setText("");
                    cbagama.setSelectedIndex(-0);
                    txtnotelepon.setText("");
                    txtemail.setText("");
                    txtasalsekolah.setText("");
                    txtalamat.setText("");
                    txtkodepos.setText("");
                    txtprovinsi.setText("");
                    txtkabkota.setText("");
                    txtkecamatan.setText("");
                    cbpilihan1.setSelectedIndex(-0);
                    cbpilihan2.setSelectedIndex(-0);
                    cbpilihan3.setSelectedIndex(-0);
                    txtnik.requestFocus();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                    TampilData();
                 }
                    stat.close();
                } catch (Exception exc) {
                    System.err.println("Terjadi Kesalahan :" + exc);
             }
        } 
    }//GEN-LAST:event_bsimpanActionPerformed

