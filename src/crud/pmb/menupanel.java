    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin mengubah data ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
        String jeniskelamin = null;
        if (rblaki.isSelected()) {
            jeniskelamin = "Laki-Laki";
        } else if (rbperempuan.isSelected()) {
            jeniskelamin = "Perempuan";
        }
        String sql = "update mahasiswabaru set nama_lengkap='" + txtnamalengkap.getText()
                + "',tempat_lahir='" + txttempatlahir.getText()
                + "',tanggal_lahir='" + txttanggallahir.getText()
                + "',jenis_kelamin='" + jeniskelamin
                + "',kewarganegaraan='" + txtkewarganegaraan.getText()
                + "',agama='" + cbagama.getSelectedItem()
                + "',no_telepon='" + txtnotelepon.getText()
                + "',email='" + txtemail.getText()
                + "',asal_sekolah='" + txtasalsekolah.getText()
                + "',alamat='" + txtalamat.getText()
                + "',kode_pos='" + txtkodepos.getText()
                + "',provinsi='" + txtprovinsi.getText()
                + "',kabkota='" + txtkabkota.getText()
                + "',kecamatan='" + txtkecamatan.getText()
                + "',prodi_satu='" + cbpilihan1.getSelectedItem()
                + "',prodi_dua='" + cbpilihan2.getSelectedItem()
                + "',pilih_kelas='" + cbpilihan3.getSelectedItem()
                + "'where NIK='" + txtnik.getText().trim() + "'";
        if (txtnik.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Data Masih Kosong!", "Ubah Data", JOptionPane.WARNING_MESSAGE);
            txtnik.requestFocus();
        } else {
            try {
                 Statement stat = koneksi.createStatement();
                 stat.executeUpdate(sql);
                 JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                 stat.close();
                 txtnik.requestFocus();
                 TampilData();
            } catch (Exception exc) {
                System.err.println("Error :" + exc);
            }
          }
        }                 
    }//GEN-LAST:event_bubahActionPerformed

